package com.exercise.studentmanagement.entity.relationship;

import com.exercise.studentmanagement.entity.Course;
import com.exercise.studentmanagement.entity.Student;
import jakarta.persistence.*;

import java.io.Serializable;

@Entity
public class StudentAndCourse implements Serializable {

    @Id
    @GeneratedValue(strategy = GenerationType.IDENTITY)
    private Long id;

    @ManyToOne
    @JoinColumn(name = "student_id")
    private Student student;

    @ManyToOne
    @JoinColumn(name = "course_id")
    private Course course;

    public StudentAndCourse() {
    }

    public StudentAndCourse(Student student, Course course) {
        this.student = student;
        this.course = course;
    }

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public Student getStudent() {
        return student;
    }

    public void setStudent(Student student) {
        this.student = student;
    }

    public Course getCourse() {
        return course;
    }

    public void setCourse(Course course) {
        this.course = course;
    }
}
